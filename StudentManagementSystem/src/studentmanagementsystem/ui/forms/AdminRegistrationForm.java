    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        // TODO add your handling code here:
        if (isAllFieldValid()) {

            String message = "Do you want to proceed to Verify?";
            // Show a confirmation dialog
            int response = JOptionPane.showConfirmDialog(null, message, "Confirmation", JOptionPane.YES_NO_OPTION);
            if (JOptionPane.YES_OPTION == response) {
                if (vod != null) {
                    vod.dispose();
                }
                vod = new VerifyOtpDialog(this, email.getText(), adminName.getText());
                vod.setModal(true);
                vod.setLocationRelativeTo(this);
                vod.setVisible(true);
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "plese make sure all field  ✅");
        }
    }//GEN-LAST:event_submitActionPerformed

    private void adminMobileNoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_adminMobileNoKeyTyped
        if (adminMobileNo.getText().trim().length() >= 10) {
            evt.consume();
        }
        allowDigit(evt);
        // check mobile numbe is valid or not
        boolean result = isMobileNoValid(adminMobileNo.getText());
        // set status label
        setLabelStatus(adminMobileNo, adminMobileNoLabel, result);
        // TODO add your handling code here:
    }//GEN-LAST:event_adminMobileNoKeyTyped

