    private boolean isAllFieldValid() {
        return adminNameLabel.getText().equals("✅")
                && adminMobileNoLabel.getText().equals("✅")
                && emailLabel.getText().equals("✅");
    }

    private void setFieldStatus() {
        setLabelStatus(adminName, adminNameLabel, false);
        setLabelStatus(adminMobileNo, adminMobileNoLabel, false);
        setLabelStatus(email, emailLabel, false);
    }

    public boolean addAdmin(String password) {
        try {
            Services.getService().addAdmin(adminName.getText(), adminMobileNo.getText(), email.getText(), password);
            return true;
        } catch (SQLException ex) {

            Logger.getLogger(AdminRegistrationForm.class.getName()).log(Level.SEVERE, null, ex);
            return false;
        }
    }

